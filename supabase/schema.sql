-- ============================================================
-- ROW LEVEL SECURITY
-- ============================================================

-- Profiles
alter table profiles enable row level security;
create policy "Public profiles are viewable by everyone" on profiles for select using (true);
create policy "Users can update own profile" on profiles for update using (auth.uid() = id);

-- Events
alter table events enable row level security;
create policy "Published events are viewable by everyone" on events for select using (status = 'published' or organizer_id = auth.uid());
create policy "Authenticated users can create events" on events for insert with check (auth.uid() = organizer_id);
create policy "Organizers can update own events" on events for update using (auth.uid() = organizer_id);

-- RSVPs
alter table rsvps enable row level security;
create policy "RSVPs are viewable by event organizer and attendee" on rsvps for select using (auth.uid() = user_id or event_id in (select id from events where organizer_id = auth.uid()));
create policy "Authenticated users can RSVP" on rsvps for insert with check (auth.uid() = user_id);
create policy "Users can update own RSVPs" on rsvps for update using (auth.uid() = user_id or event_id in (select id from events where organizer_id = auth.uid()));

-- Newsletter subscriptions (public insert, private read)
alter table newsletter_subscriptions enable row level security;
create policy "Anyone can subscribe" on newsletter_subscriptions for insert with check (true);
create policy "Admins can view subscriptions" on newsletter_subscriptions for select using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);

-- Newsletter issues (public read)
alter table newsletter_issues enable row level security;
create policy "Published issues are viewable by everyone" on newsletter_issues for select using (sent_at is not null);
create policy "Admins can manage issues" on newsletter_issues for all using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);

